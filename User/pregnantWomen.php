      <th>Example serving size </th>
    </tr>
    <tr>
      <td>Vegetables and legumes/beans </td>
      <td>18 years or under: 5 
19–50 years: 5</td>
      <td>½ cup cooked vegetables 
½ cup cooked or canned* beans, peas or lentils
1 cup green leafy or raw salad vegetables
½ cup sweet corn
½ medium potato or other starchy vegetables 
1 medium tomato</td>
    </tr>
    <tr>
      <td>Fruit</td>
      <td>18 years or under: 2 
19–50 years: 2</td>
      <td>1 medium fruit, such as apple, banana, orange
2 small fruits, such as apricots, kiwi fruits or plums
1 cup diced or canned fruit (no added sugar) 

Or only occasionally

125 ml (½ cup) fruit juice (no added sugar)
30 g dried fruit (such as 4 apricot halves, 1 ½ tablespoons sultanas)</td>
    </tr>
    <tr>
      <td>Grain (cereal) foods, mostly wholegrain and/or high-fibre varieties</td>
      <td>18 years or under: 8 
19–50 years: 8½</td>
      <td>1 slice bread, ½ medium roll or flat bread (40 g)
½ cup cooked rice, pasta, noodles, barley, buckwheat, semolina, polenta, burghul or quinoa
½ cup cooked porridge, 2/3 cup wheat cereal flakes, ¼ cup muesli
3 crispbreads
1 crumpet, small English muffin or scone</td>
    </tr>
    <tr>
      <td>Lean meats and poultry, fish, eggs, tofu, nuts and seeds and legumes/beans</td>
      <td>18 years or under: 3½ 
19–50 years: 3 ½</td>
      <td>65 g cooked lean meats, such as beef, lamb, veal, pork, goat or kangaroo 
(90–100 g raw)
80 g cooked lean poultry, such as chicken, turkey (100 g raw)
100 g cooked fish fillet (115 g raw) or one small can of fish
2 large eggs
1 cup cooked or canned* legumes/beans, such as lentils, chickpeas or split peas
170 g tofu
30 g nuts or seeds, nut/seed paste*</td>
    </tr>
    <tr>
      <td>Milk, yoghurt, cheese and/or alternatives, mostly reduced fat</td>
      <td>18 years or under: 3 ½ 
19–50 years: 2 ½ </td>
      <td>1 cup (250 ml) fresh, UHT long-life, reconstituted powdered milk or buttermilk
½ cup (120 ml) evaporated milk
2 slices (40 g) hard cheese, such as cheddar
¾ cup (200 g) yoghurt
1 cup (250 ml) soy, rice or other cereal drink, with at least 100 mg of added calcium per 100 ml</td>
    </tr>
