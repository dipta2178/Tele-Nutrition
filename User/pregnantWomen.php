  <h2 class="w3-wide"><b>The following foods are beneficial to your health and fetal development during pregnancy:</b></h2>

  <ul class="w3-ul" style="width:90%">
  <li><span class="w3-badge">1</span> Vegetables: carrots, sweet potatoes, pumpkin, spinach, cooked greens, tomatoes and red sweet peppers (for vitamin A and potassium)</li>
  <li><span class="w3-badge">2</span> Fruits: cantaloupe, honeydew, mangoes, prunes, bananas, apricots, oranges, and red or pink grapefruit (for potassium)</li>
  <li><span class="w3-badge">3</span> Dairy: fat-free or low-fat yogurt, skim or 1% milk, soymilk (for calcium, potassium, vitamins A and D)</li>
  <li><span class="w3-badge">4</span> Grains: ready-to-eat cereals/cooked cereals (for iron and folic acid)</li>
  <li><span class="w3-badge">5</span> Proteins: beans and peas; nuts and seeds; lean beef, lamb and pork; salmon, trout, herring, sardines and pollock</li>
</ul>
  <br>

  <h2 class="w3-wide"><b>Fluid Intake During Pregnancy:</b></h2>
  <p class="w3-justify"><b>Fluid intake is also an important part of pregnancy nutrition. Follow these recommendations for fluid intake during pregnancy:</b></p><br>
  <ul class="w3-ul">
  <li><span class="w3-badge">1</span> You can take in enough fluids by drinking several glasses of water each day, in addition to the fluids in juices and soups. Talk to your health care provider or midwife about restricting your intake of caffeine and artificial sweeteners.</li>
  <li><span class="w3-badge">2</span> Avoid all forms of alcohol.</li>
</ul><br>
